package javahomework;

public class practiceFORLOOP {

	public static void main(String[] args) {
		
	

		
//print even num 0 to 20
		for(int a=0;a<=20;a=a+2) {
			System.out.println("the even number is:"+a);
			
			
		}
		//print odd number 4 to 30
		for(int y=5;y<=30;y=y+2) {
			
			
			System.out.println("the  number is odd:" +y);
			
			
			
		}
	}

}
