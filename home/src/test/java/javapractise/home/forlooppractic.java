package javapractise.home;

public class forlooppractic {

	public static void main(String[] args) {
		// use a for loop to print i am a student of nextteach"5 times"
		for(int i=1;i<=5;i++) {
			System.out.println("i am student of nextteach itc");
		}
		//print odd number from 4 to 30
		for(int a=5;a<=30;a+=5) {
			System.out.println("the result is odd:"+a);
		}
		//print even number from 0 to 12
		for(int j=0;j<=12;j+=4) {
			System.out.println("the result is even:"+j);
		}
		//make a code for numbers table
		for(int k=1;k<=5;k++) {
			int num=2*k;
			
			System.out.println("2x"+k+"="+num);
		}
		

	}

}
